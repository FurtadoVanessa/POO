public class Televisao {

    private int volume;
    private int canal;

    public int getVolume(){
        return this.volume;
    }

    public void setVolume(int volume){
        this.volume = volume;
    }

    public int getCanal(){
        return this.canal;
    }

    public void setCanal(int canal){
        this.canal = canal;
    }
}
