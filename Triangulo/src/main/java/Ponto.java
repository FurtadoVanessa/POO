public class Ponto {

    private int coordenadaX;
    private int coordenadaY;

    public Ponto(int x, int y){
        this.coordenadaX = x;
        this.coordenadaY = y;
    }

    public int getX(){
        return this.coordenadaX;
    }

    public int getY(){
        return this.coordenadaY;
    }




}
